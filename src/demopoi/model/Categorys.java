package demopoi.model;

import java.io.Serializable;
import java.util.Collection;
import javax.persistence.Basic;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.OneToMany;
import javax.persistence.Table;

/**
 *
 * @author cjuan
 */
@Entity
@Table(name = "categorys")
@NamedQueries({
    @NamedQuery(name = "Categorys.findAll", query = "SELECT c FROM Categorys c"),
    @NamedQuery(name = "Categorys.findById", query = "SELECT c FROM Categorys c WHERE c.id = :id"),
    @NamedQuery(name = "Categorys.findByName", query = "SELECT c FROM Categorys c WHERE c.name = :name"),
    @NamedQuery(name = "Categorys.findByDescription", query = "SELECT c FROM Categorys c WHERE c.description = :description")})
public class Categorys implements Serializable {

    private static final long serialVersionUID = 1L;
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    @Basic(optional = false)
    @Column(name = "id")
    private Integer id;
    @Column(name = "name")
    private String name;
    @Column(name = "description")
    private String description;
    @OneToMany(mappedBy = "category")
    private Collection<Products> productsCollection;

    public Categorys() {
    }

    public Categorys(Integer id) {
        this.id = id;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Collection<Products> getProductsCollection() {
        return productsCollection;
    }

    public void setProductsCollection(Collection<Products> productsCollection) {
        this.productsCollection = productsCollection;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (id != null ? id.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Categorys)) {
            return false;
        }
        Categorys other = (Categorys) object;
        if ((this.id == null && other.id != null) || (this.id != null && !this.id.equals(other.id))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "demopoi.util.Categorys[ id=" + id + " ]";
    }
    
}
